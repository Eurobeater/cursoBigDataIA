x = 6
y = 2

print(f"{x} mas {y} es igual a {x + y}")
print(f"{x} menos {y} es igual a {x - y}")
print(f"{x} por {y} es igual a {x * y}")
print(f"{x} entre {y} es igual a {x / y}")

z = 7
#Obtener el cociente de y. Es decir, obtener la division entera
print(f"{z} dividido {y} es igual a {z // y}")

#Resto de la division
print(f"{z} dividido {y} es igual a {z % y}")

#Potencia
print(f"{x} elevado a {y} es igual a {x ** y}")
print(f"{x} elevado a {3} es igual a {x ** 3}")

#Raiz cuadrada
print(f"La raíz cuadrada de {x} es igual a {x ** 0,5}") #Es lo mismo que n(Raiz cuadrada) de a elevado a m
print(f"La raíz cúbica de {x} es igual a {x ** (1/3)}")